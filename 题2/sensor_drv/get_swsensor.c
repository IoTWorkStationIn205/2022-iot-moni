#include "ioCC2530.h"
#include "hal_defs.h"
#include "get_swsensor.h"


/******************************************************************************
 * GLOBAL VARIABLES
 */

/******************************************************************************
 * 名称       get_swsensor
 * 功能       读取开关量值
 * 入口参数   无
 * 出口参数   0或1电平
 *****************************************************************************/
uint8 get_swsensor(void)
{
    P0SEL &= ~( 1 <<1);  //设置p0.1为普通i/o口
    P0DIR &= ~( 1 <<1);  //设置p0.1为输入口
    return P0_1;         //返回p0.1电平
}

























